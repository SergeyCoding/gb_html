export function getBathrooms(page, pageLimit) {
  return getData(getProjects().bathrooms, page, pageLimit);
}
export function getBedrooms(page, pageLimit) {
  return getData(getProjects().bedrooms, page, pageLimit);
}
export function getKitchens(page, pageLimit) {
  return getData(getProjects().kithens, page, pageLimit);
}
export function getLivingAreas(page, pageLimit) {
  return getData(getProjects().livingAreas, page, pageLimit);
}

function getData(data, page, pageLimit) {
  const result = { data: [], pages: 0 };

  result.pages = data.length / pageLimit + 1;

  for (let i = (page - 1) * pageLimit; i < data.length; i++) {
    const element = data[i];
    result.data.push(element);
    if (i >= page * pageLimit) {
      break;
    }
  }

  return result;
}
